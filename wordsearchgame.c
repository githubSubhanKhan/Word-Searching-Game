int playerrecord() {
    FILE *fptr;
    fptr = fopen( "player_record.txt","a+");
    if ( fptr == NULL ) {
        printf("Error Opening in File!");
        return 1;
    }
    fprintf( fptr ,  "---------------------------------\n\n\n");
    fprintf( fptr ,  "Player Name: %s \n\n\n", fullname );
    fprintf( fptr ,  "Roll Number: %s \n\n\n", rollno );
    fprintf( fptr ,  "Final Score: %d \n\n\n", score );
    fclose( fptr );
}

